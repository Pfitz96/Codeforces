polyhedrons = {'Tetrahedron': 4,'Cube': 6,'Octahedron': 8,'Dodecahedron': 12,'Icosahedron': 20}
n = int(input())
count = 0
for i in range(n):
    x = input()
    for p,v in polyhedrons.items():
        if x == p:
            count = count + v
        else:
            count += 0
print(count)

