t = int(input())
while t > 0:
    s = input()
    zeros = s.replace("0", "")
    ones = s.replace("1", "")
    v = min(len(zeros), len(ones))
    if v % 2 == 0:
        print("NET")
    else:
        print("DA")
    t -= 1

